/* Assignment (=): Assigns a value to a variable.
Addition Assignment (+=): Adds the right operand to the left operand and assigns the result to the left operand. Same goes with -,* and /
 */

let a=10; // declaring and initializing the variable 'a' with the value 10

a+=10;
console.log(a);  // outputs: 20

a-=10;
console.log(a);  // outputs: 10

a/=5;
console.log(a);  // outputs: 2

a*=2;
console.log(a);  //outputs :4