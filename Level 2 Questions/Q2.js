/*    Declare a variable day and assign it a string value representing the current day of the week.
    Write a switch statement to check the value of day and print a corresponding message. */

    let day="Tuesday";

    switch(day){
        case "Monday":console.log("Today is monday");
        break;
        case "Tuesday":console.log("Today is tuesday");
        break;
        case "Wednesday":console.log("Today is wednesday");
        break;
        case "Thrusday":console.log("Today is thursday");
        break;
        case "Friday":console.log("Today is friday");
        break;
        case "Saturday":console.log("Today is saturday");
        break;
        case "Sunday":console.log("Today is sunday");
        break;
    }